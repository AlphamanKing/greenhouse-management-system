                <div class="col-lg-4">
                    <span><h2> Sanitation</h2> </span>
                    <p>Proper sanitation is critical in the greenhouse or high tunnel environment.
                        Weeds, which may harbor insect pests and some pathogens, and also reduce air movement,
                        should be removed from inside and outside the structure. Diseased tissue should be removed and
                        disposed of.
                        Cull piles are a source of inoculum and waste tissue should be composted or buried.
                        If composting, choose a composting process that facilitates rapid decomposition and
                        maintains the necessary high temperatures throughout the pile � preferably an active process
                        in which the pile is turned frequently. The composting cull pile should be located as far away
                        from the greenhouse or high tunnel as possible. </p>
                    <p>Workers who smoke should dip their hands into whole milk, followed by thorough washing in hot
                        soapy water to inactivate tobacco mosaic virus, which may infect tomatoes. Workers should wear
                        coveralls that are laundered often and stored in a smoke-free facility.</p>
                    <p>Bacterial canker is seedborne, so in order to avoid the possibility of transmission of this
                        important pathogen from seed to seedlings to plants, seed should be hot water-treated prior to
                        planting. Complete instructions for hot water treatment of vegetable seeds (Miller and Ivey
                        2005)
                        are available</p>


                </div>

                <div class="col-lg-4">
                    <span><h2> Environmental management</h2> </span>

                    <p>All of the foliar fungal diseases are favored by high relative humidity in the tomato canopy.
                        Wider plant spacing and improved ventilation help to reduce the incidence of these diseases.
                        Tomato plants should be suckered below the first fruit set to promote air circulation.
                        Removing the lower leaves as the plant ages also increases air movement around the plants.
                        For high tunnels, the sides and ends should remain open as much as possible to promote air
                        movement.
                        Workers should avoid handling plants when free moisture is present to reduce the spread of
                        pathogens
                        from diseased to healthy plants. Plants should be irrigated without applying water to the
                        foliage.
                        In greenhouses, temperatures should be managed to reduce condensation, and fans or tubing can be
                        used to distribute heated air throughout the structure.</p>
                </div>
                <div class="col-lg-4">
                    <span><h2>Resistant Varieties</h2> </span>
                    <p> No tomato variety is resistant to every disease that may appear in the greenhouse or high
                        tunnel.
                        In fact, there are no commercially available sources of resistance to some of the most important
                        diseases, such as bacterial canker and white mold. Further, very few heirloom varieties, often
                        favored
                        by organic producers, are resistant to diseases. Check with the seed supplier to determine if
                        varieties
                        with resistance to a problematic disease are available. Organic growers must use organically
                        produced
                        seed if available, but untreated seed that is conventionally produced is permitted if
                        organically-produced
                        stocks cannot be obtained. For more information on organic seed and suppliers, see Sourcing
                        Certified Organic
                        Seed and The National Organic Program (NOP) Regulations.</p>
