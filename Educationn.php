                <div class="row col-lg-4">
                    <div id="main">
                        <span><h3><b>Soil quality and soilborne disease management</b></h3></span>

                        <p>Organic tomatoes may be produced in �ground� culture on raised or flat beds in soil,
                            in bag/pot culture using soilless planting mix, or in hydroponic culture. In the latter two
                            systems,
                            tomatoes are produced in a soilless planting mix or medium and fed nutrients in liquid form.
                            A clear advantage of the soilless culture methods is the ability to replace contaminated
                            planting mix
                            and avoid many soilborne pathogens (although pathogens such as Pythium sp. can be a serious
                            problem
                            in bag/pot and hydroponic culture). For soil culture, improving soil quality is critical for
                            optimal
                            soil fertility and crop health. The addition of good quality compost is essential for
                            increasing soil
                            organic matter and providing nutrients for the crop. Further, research has shown that
                            increased organic matter results
                            in a more extensive and varied microbial community, resulting in suppression of soilborne
                            pathogens and improved plant health.
                        </p>
                    </div>
                </div>

                <div class="col-lg-4">
                    <span><h2>Rotation </h2> </span>

                    <p>Crop rotation is a cornerstone of disease management
                        for organic crops, but may be difficult to achieve in greenhouses
                        and high tunnels utilizing soil culture. High tunnels can be built as
                        temporary structures that can be moved from one location to another (Figure 12).
                        These high tunnels must be securely anchored in order to prevent structural damage
                        during high winds. For immovable structures, crops should be rotated within the greenhouse
                        between (not among!) plant families. For example, tomatoes should be rotated with vine crops,
                        lettuce, or cole crops, for example</p>
                    <div class="image">
                        <img src="edu/tdisease192.jpg" height="220px" width="338px">
                        <p>Figure 12: Hydraulic drill to secure anchors for moveable high tunnel.</p>
                    </div>
                </div>
                <div class="col-lg-4">
                    <span><h2>white mold Management tip</h2> </span>
                    <p>When white mold is observed in a greenhouse or high tunnel,
                        the diseased plants and soil immediately surrounding the stem
                        (approx. 6-8 inch radius, 4-6 in. deep) that may contain sclerotia (Figure 13)
                        should be removed immediately from the greenhouse and buried deeply in another location.
                        Growers may also consider applying the biocontrol agent Contans to soil after the tomato
                        crop to reduce the viability and number of sclerotia.</p>
                    <div class="image">
                        <img src="edu/tdisease20.jpg" height="220px" width="338px">
                        <p>Figure 13: Sclerotia (arrows) of the white mold pathogen surrounding the base of the diseased
                            tomato plant.</p>
