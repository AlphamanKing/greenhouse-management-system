                <h2>Introduction</h2>
                <p>Production of tomatoes in greenhouses and <a href="/pages/18358/introduction-to-high-tunnels"
                                                                class="internal_link">high tunnels</a> has increased in
                    popularity significantly in the last decade. Both provide the option of off-season production and
                    expansion of markets over traditional outdoor field systems. While greenhouse tomato culture tends
                    to be very high-tech and capital-intensive, high tunnels expand the growing season in both spring
                    and fall with much lower capital investments. Greenhouse and high tunnel systems pose unique pest
                    management challenges. The �protected culture� of greenhouse and high tunnel production may result
                    in lower incidence of diseases exacerbated by rainfall, such as Septoria leaf spot and bacterial
                    spot and speck. However, diseases that are uncommon in open fields may appear under protected
                    culture. Botrytis blight/gray mold (Figure 1), white mold (timber rot) (Figure 2) and leaf mold
                    (Figure 3) are among the most important of these diseases. Late blight (Figure 4) may also occur
                    under cool, moist conditions, while powdery mildew has occurred in many greenhouses under drier
                    conditions (Figure 5).</p>
