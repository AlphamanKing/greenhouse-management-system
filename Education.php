                <p>These diseases can be managed by employing appropriate integrated pest management (IPM) approaches
                    including cultural tactics, resistant varieties and applications of materials approved for use in
                    organic production systems. For more information on disease management materials, see the eOrganic
                    article <a href="/pages/18360/can-i-use-this-product-for-disease-management-on-my-organic-farm"
                               class="internal_link">Can I Use This Product for Disease Management on my Organic
                        Farm?</a>.</p>
                <h2>Management Strategies</h2>
                <h3>Cultural Tactics</h3>
                <p><b>Soil quality and soilborne disease management</b></p>
                <p>Organic tomatoes may be produced in �ground� culture on raised or flat beds in soil, in bag/pot
                    culture using soilless planting mix, or in hydroponic culture. In the latter two systems, tomatoes
                    are produced in a soilless planting mix or medium and fed nutrients in liquid form. A clear
                    advantage of the soilless culture methods is the ability to replace contaminated planting mix and
                    avoid many soilborne pathogens (although pathogens such as <i>Pythium</i> sp. can be a serious
                    problem in bag/pot and hydroponic culture). For soil culture, improving soil quality is critical for
                    optimal soil fertility and crop health. The addition of <a
                            href="http://ohioline.osu.edu/anr-fact/0015.html" class="external_link">good quality
                        compost</a> is essential for increasing soil organic matter and providing nutrients for the
                    crop. Further, research has shown that increased organic matter results in a more extensive and
                    varied microbial community, resulting in suppression of soilborne pathogens and improved plant
                    health. Our research has shown that tomato plants grown in compost-amended soil in high tunnels had
                    significantly less white mold than those grown in non-amended soil. Early blight was also reduced on
                    tomatoes grown in compost-amended soil compared to those grown in non-amended soil in high tunnels.
                    If a soilborne pathogen problem develops, soil can be pasteurized or <a
                            href="http://attra.ncat.org/attra-pub/gh-ipm.html#disease"
                            class="external_link">sterilized</a> by a number of methods, although these can be costly.
                    Details on soil and soilless culture methods can be found in ATTRA publications <a
                            href="http://attra.ncat.org/attra-pub/summaries/ghtomato.html" class="external_link">Organic
                        Greenhouse Tomato Production</a>, <a href="http://attra.ncat.org/attra-pub/summaries/ghveg.html"
                                                             class="external_link">Organic Greenhouse Vegetable
                        Production</a>, and <a href="http://attra.ncat.org/attra-pub/summaries/gh-ipm.html"
                                               class="external_link">Integrated Pest Management for Greenhouse Crops</a>.
                    For more information on organic soil and fertility management see the eOrganic articles <a
                            href="/pages/18566/conventional-chemical-soil-testing-in-organic-farming-systems"
                            class="internal_link">Conventional Chemical Soil Testing within Organic Systems</a> and <a
                            href="/pages/18565/organic-soil-fertility" class="internal_link">Organic Soil
                        Fertility</a><a href="/pages/18634/use-of-tillage-in-organic-farming-systems:-the-basics"
                                        class="internal_link">.</a></p>
                <p><b>Rotation</b></p>
                <p>Crop rotation is a cornerstone of disease management for organic crops, but may be difficult to
                    achieve in greenhouses and high tunnels utilizing soil culture. <a
                            href="/pages/18369/construction-of-high-tunnels:-resources-for-organic-farmers"
                            class="internal_link">High tunnels</a> can be built as temporary structures that can be
                    moved from one location to another (Figure 12). These high tunnels must be securely anchored in
                    order to prevent structural damage during high winds. For immovable structures, crops should be
                    rotated within the greenhouse between (not among!) plant families. For example, tomatoes should be
                    rotated with vine crops, lettuce, or cole crops, for example. Unfortunately, there are some
                    pathogens, such as <i>Sclerotinia sclerotiorum</i>, the causal agent of white mold (also known as
                    timber rot), that have a wide host range and attack plants in different families. It is therefore
                    important to scout plants regularly and know which diseases are present in the crop.</p>
                <p><img height="237" width="316" alt=""
                        src="http://eorganic.info/sites/eorganic.info/files/u260/tdisease19.jpg"></p>
                <p><i>Figure 12: Hydraulic drill to secure anchors for moveable high tunnel. Figure credit: Sally A.
                        Miller, The Ohio State University</i></p>
                <p>*Management tip* When white mold is observed in a greenhouse or high tunnel, the diseased plants and
                    soil immediately surrounding the stem (approx. 6-8 inch radius, 4-6 in. deep) that may contain
                    sclerotia (Figure 13) should be removed immediately from the greenhouse and buried deeply in another
                    location. Growers may also consider applying the biocontrol agent Contans to soil after the tomato
                    crop to reduce the viability and number of sclerotia.</p>
                <p><img height="237" width="316" alt=""
                        src="http://eorganic.info/sites/eorganic.info/files/u260/tdisease20.jpg"></p>
                <p><i>Figure 13: Sclerotia (arrows) of the white mold pathogen surrounding the base of the diseased
                        tomato plant. Removing the plant and the soil surrounding the base from the high tunnel or
                        greenhouse will remove inoculum. Figure credit: Sally A. Miller, The Ohio State University</i>
                </p>
                <p><b>Sanitation</b></p>
                <p>Proper sanitation is critical in the greenhouse or high tunnel environment. Weeds, which may harbor
                    insect pests and some pathogens, and also reduce air movement, should be removed from inside and
                    outside the structure. Diseased tissue should be removed and disposed of. Cull piles are a source of
                    inoculum and waste tissue should be composted or buried. If composting, choose a composting process
                    that facilitates rapid decomposition and maintains the necessary high temperatures throughout the
                    pile � preferably an active process in which the pile is turned frequently. The composting cull pile
                    should be located as far away from the greenhouse or high tunnel as possible.</p>
                <p>Surfaces should be cleaned thoroughly after each crop, and tools should be cleaned regularly. Workers
                    should wash hands often � at least at the end of each row � to minimize spread of pathogens,
                    particularly Botrytis grey mold, bacterial canker, TMV, PepMV and TCDVd. In some operations, workers
                    wear gloves that can be regularly dipped into a sanitizing agent. Tomato greenhouse operations
                    routinely use disinfectant-filled footbaths to prevent movement of pathogens on boots and shoes into
                    the greenhouse. Pruning tools and knives are available that dispense a disinfectant solution during
                    cutting and pruning operations, significantly reducing the risk of disease spread. There are a
                    number of disinfectants/sanitizers permitted for use on organic farms. For more information, see the
                    eOrganic articles <a href="/pages/18355/approved-chemicals-for-use-in-organic-postharvest-systems"
                                         class="internal_link">Approved Chemicals for Use in Organic Postharvest
                        Systems</a> and <a href="/pages/18321/can-i-use-this-input-on-my-organic-farm"
                                           class="internal_link">Can I Use This Input on my Organic Farm?</a>.
                    Unfortunately, there are few independent studies available to guide the selection of a disinfectant
                    or sanitizer for organic food crop production.<i><br>
                    </i></p>
                <p>Workers who smoke should dip their hands into whole milk, followed by thorough washing in hot soapy
                    water to inactivate tobacco mosaic virus, which may infect tomatoes. Workers should wear coveralls
                    that are laundered often and stored in a smoke-free facility.</p>
                <p>Bacterial canker is seedborne, so in order to avoid the possibility of transmission of this important
                    pathogen from seed to seedlings to plants, seed should be hot water-treated prior to planting.
                    Complete instructions for <a href="http://ohioline.osu.edu/hyg-fact/3000/pdf/3086.pdf"
                                                 class="external_link">hot water treatment of vegetable seeds</a>
                    (Miller and Ivey 2005) are available.</p>
                <p><b>Environmental management</b></p>
                <p>All of the foliar fungal diseases are favored by high relative humidity in the tomato canopy. Wider
                    plant spacing and improved ventilation help to reduce the incidence of these diseases. Tomato plants
                    should be suckered below the first fruit set to promote air circulation. Removing the lower leaves
                    as the plant ages also increases air movement around the plants. For high tunnels, the sides and
                    ends should remain open as much as possible to promote air movement. Workers should avoid handling
                    plants when free moisture is present to reduce the spread of pathogens from diseased to healthy
                    plants. Plants should be irrigated without applying water to the foliage. In greenhouses,
                    temperatures should be managed to reduce condensation, and fans or tubing can be used to distribute
                    heated air throughout the structure.</p>
                <h3>Resistant Varieties</h3>
                <p>No tomato variety is resistant to every disease that may appear in the greenhouse or high tunnel. In
                    fact, there are no commercially available sources of resistance to some of the most important
                    diseases, such as bacterial canker and white mold. Further, very few heirloom varieties, often
                    favored by organic producers, are resistant to diseases. Check with the seed supplier to determine
                    if varieties with resistance to a problematic disease are available. Organic growers must use
                    organically produced seed if available, but untreated seed that is conventionally produced is
                    permitted if organically-produced stocks cannot be obtained. For more information on organic seed
                    and suppliers, see <a
                            href="/pages/18338/sourcing-certified-organic-seed-and-the-national-organic-program-regulations"
                            class="internal_link">Sourcing Certified Organic Seed and The National Organic Program (NOP)
                        Regulations</a>.</p>
                <p>Nearly all of the tomatoes used in conventional greenhouse tomato production are grafted. The
                    rootstocks used are not particularly disease-resistant, but impart higher vigor and prolonged plant
                    life. Organic growers are beginning to experiment with <a
                            href="http://www4.ncsu.edu/~clrivard/TubeGraftingTechnique.pdf" class="external_link">grafting
                        for soil-cultured tomatoes</a> (Rivard and Louws 2006) particularly in areas where bacterial
                    wilt, root knot nematode or other soilborne diseases are a problem. Preferred varieties are grafted
                    onto disease-resistant rootstocks and allowed to heal before transplanting. While the cost of
                    grafted transplants is higher than that of non-grafted ones, where soilborne diseases are a problem,
                    this option may be more cost-effective than soil pasteurization or sterilization.</p>
                <h3>Fungicides</h3>
                <p>All of the diseases mentioned are difficult to manage once they become established, and the
                    fungicides allowed in organic production usually cannot tame a raging disease epidemic. However, if
                    diseases are identified early in the epidemic and all the appropriate cultural tactics have been
                    employed, approved fungicides may be applied to reduce disease spread throughout the greenhouse or
                    high tunnel. For powdery mildew, products containing fixed copper or sulfur can be effective. Fixed
                    copper products are also effective in reducing the incidence and severity of leaf mold and early
                    blight. Plant extracts and various biocontrol agents have shown some efficacy in reducing diseases
                    in organic tomatoes, but they are generally less effective than fixed copper fungicides. However,
                    coppers should not be overused as they may accumulate in soil and are toxic at high levels to soil
                    flora and fauna.</p>
                <p>NOTE: Before applying ANY pest control product, be sure to 1) read and understand the safety
                    precautions and application restrictions, and 2) make sure that the brand name product is listed
