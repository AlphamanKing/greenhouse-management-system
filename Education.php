                <p>Viruses and viroids can wreak havoc in a greenhouse or high tunnel. Some, such as tomato spotted wilt
                    virus (Figure 9) and <a href="http://www.sciencedaily.com/releases/2007/11/071130200450.htm"
                                            class="external_link">tomato marchitez virus</a> are transmitted by insect
                    vectors, while others, such as tobacco mosaic virus (TMV, Figure 10) and pepino mosaic virus are
                    mechanically transmitted (transmitted by people or tools that rub up against or cut or damage plant
                    tissues). Still others are seedborne. Several viroids including tomato chlorotic dwarf viroid
                    (TCDVd), Mexican pepita viroid and tomato planta macho viroid (Figure 11) have been identified in
                    recent years causing severe problems in greenhouse tomatoes. The viroids are seedborne but are also
                    easily mechanically transmitted.</p>
